package com.mango.arithmetic;

public class Demo02Arithmetic {
    public static void main(String[] args) {
        int i = 10;
        int j = 3;
        System.out.println("i + j = " + (i + j + ""));
        System.out.println("i + j = " + (i + j + "" + 1));
        System.out.println("i + j = " + (i + "" + j));
    }
}
