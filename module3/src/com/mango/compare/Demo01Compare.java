package com.mango.compare;

public class Demo01Compare {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println("a==b " + (a == b)); // false
        System.out.println("a!=b " + (a != b)); // true
        System.out.println("a>b " + (a > b)); // false
        System.out.println("a<b " + (a < b)); // true
        System.out.println("a>=b " + (a >= b)); // false
        System.out.println("a<=b " + (a <= b)); // true
        System.out.println("----------------------");
    }
}

